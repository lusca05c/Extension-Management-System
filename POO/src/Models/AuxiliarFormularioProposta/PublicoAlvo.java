package Models.AuxiliarFormularioProposta;

public class PublicoAlvo {
	private String descricao;
	private int quantidadeBeneficiada;
	
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	public int getQuantidadeBeneficiada() {
		return quantidadeBeneficiada;
	}
	public void setQuantidadeBeneficiada(int quantidadeBeneficiada) {
		this.quantidadeBeneficiada = quantidadeBeneficiada;
	}
	
}
