package Models.AuxiliarFormularioAvaliacao;


public class IdentificacaoProposta {
	
	private String atendeTitulo;
	private String consideracoesTitulo;
	private String consideracoesObjetivos;
	
	public String getAtendeTitulo() {
		return atendeTitulo;
	}
	public void setAtendeTitulo(String atendeTitulo) {
		this.atendeTitulo = atendeTitulo;
	}
	public String getConsideracoesTitulo() {
		return consideracoesTitulo;
	}
	public void setConsideracoesTitulo(String consideracoesTitulo) {
		this.consideracoesTitulo = consideracoesTitulo;
	}
	public String getConsideracoesObjetivos() {
		return consideracoesObjetivos;
	}
	public void setConsideracoesObjetivos(String consideracoesObjetivos) {
		this.consideracoesObjetivos = consideracoesObjetivos;
	}
	
}
